        <div class="information-desc mb-3">
            <p>
                在我大学时，上纪录片课，有一部纪录片是必讲的，就是于2001年上映的法国纪录片——《鸟的迁徙》。这部纪录片历时三年，有600多人参与拍摄，走遍全球50多个国家和地区。但最令人肃然起敬的是，因为摄制组在拍摄之初发现即使动用当时最先进的噪声很小的滑翔机设备，鸟儿依然因惧怕机器而极力逃离摄影范围，于是整个主创团队开始亲自养鸟。他们用人工孵化鸟类，长期喂养，直到鸟儿们已经熟悉摄影工作人员，不怕摄影机靠近时，才开始拍摄，他们与鸟儿们共同生活了三年。
            </p>
            <p>
                最后呈现出的，是一部诗一般优雅而宏大的影片，虽然拍摄对象是鸟类，却洋溢着人文主义精神的光华。透过唯美的画面、令人意想不到的戏剧性真实，观众看到的是摄制者们对自然深沉的爱与守候。正如这部影片的第一句话：这是一个关于承诺的故事。
            </p>
            <p>
                由此我意识到，其实所有优秀的自然类纪录片，都是一个承诺，是摄制者对于万物生灵无限关怀敬畏的承诺，是以平等真实的视角歌咏自然的承诺。
            </p>
            <p>
                近几年，不断有优秀的自然类纪录片涌现，在豆瓣上，故事片类评分高达9.3分以上的不过凤毛麟角，但《地球脉动2》和《蓝色星球2》都打到了9.9的顶级高分。这两部都由品质认证机构BBC出品，而今年，BBC又推出了一部重量级纪录片——《王朝》。
            </p>
            <p>
                这部《王朝》是腾讯视频和BBC联合出品，目前播放量已超过1.4亿。曾几何时，纪录片是一个鲜少有人问津的领域，北京师范大学纪录片研究中心曾于2014年发布过一份《中国纪录片公众形象调查》，调查显示：48%的城市居民不知道何为纪录片，28.3%的公众近半年内没有看过纪录片，而学历越高的观众，对纪录片的认知度越高。可以说，在电影领域，纪录片是一种曲高和寡的存在，收入的增速远远比不上成本，也正因如此，愿意专注投身于纪录片创作的机构少之又少，与此同时，付出漫长时间与精力拍摄纪录片的创作者们，才更加令人充满敬仰。
            </p>
            <p>
                在我们小的时候，最熟悉的关于自然的纪录莫过于央视的《动物世界》，一定有很多人对赵忠祥老师的“春天来了，万物复苏，大草原上又到了动物交配的季节”记忆犹新。在过去，由于摄影器材和人们自然意识的限制，对自然的纪录主要偏向于展示和猎奇，但随着技术及自然意识的不断进步，纪录片开始以一种“人性”的视角看待自然，发现动物族群中的戏剧性和某些精神特质，比如在《帝企鹅日记》里，从一对企鹅夫妇的角度，用拟人化的口吻展现了企鹅父母为繁育后代而付出的艰辛，它呈现出的是一种高于动物性的，曾经人们认为只有人类才具有的爱与亲情。
            </p>
            <p>
                而这部《王朝》，正如它的名字，表现的是动物群落中的权力争夺和王朝创建。毫不夸张的说，这就是一部动物版的《权利与游戏》，而其极具张力的故事性和声画的恢弘程度，也完全无愧于这个称号。这部纪录片不仅拍到了奇景、故事，甚至还拍到了微表情。在第一集里，我们看到了一只黑猩猩的老谋深算与卧薪尝胆，当镜头特写猩猩“戴维”深沉低望的一个抬头，那样隐忍又饱含权谋的王者之气，令人不敢相信这只是一只猩猩；当猩猩内部大打出手，那种悲壮、血腥、紧张，又仿佛真实的人类战争。
            </p>
            <p>
                猩猩之间也有拉拢人心、兄弟反目，有友谊的建立也有对交配权的争夺，在这群黑猩猩身上，你几乎可以看到人类世界的一切元素，个性、政治、生存、竞争……这种真实是令人畏惧的，因为它和人类世界太过相似。
            </p>
            <p>
                如果说我们从《帝企鹅日记》、《蓝色星球》这类纪录片里能看到动物的“人性”，那么从《王朝》里，我们能看到的是人类的“兽性”。对于权力的追逐与捍卫，对于家族的守护与背叛，对于生存的顽强与抉择……这些我们称之为“文明”的东西，原来并非人类独有，我们千百年来咏叹的所谓“人间悲喜”，实际在人类之外的世界，也在此起彼伏的上演。
            </p>
           </div>

